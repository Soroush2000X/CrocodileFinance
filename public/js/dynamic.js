// Get the container element
var btnContainer = document.getElementsByClassName("plans");

// Get all buttons with class="btn" inside the container
var btns = btnContainer.getElementsByClassName("plan-btn");

// Loop through the buttons and add the active class to the current/clicked button
for (var i = 0; i < btns.length; i++) {
  btns[i].addEventListener("click", function() {
    var current = document.getElementsByClassName("plan-btn-active");
    current[0].className = current[0].className.replace("plan-btn-active", "");
    this.className += "plan-btn-active";
  });
}


